git push --set-upstream origin master 
#다음의 명령은 로컬 브랜치를 원격 저장소의 마스터 브랜치로 푸시하면서, 로컬 브랜치와 원격 브랜치를 연결하는 명령입니다. 이 명령을 사용하면 이후에 git push 명령을 사용할 때 로컬 브랜치와 원격 브랜치를 자동으로 연결할 수 있습니다.

# 여기에서 몇 가지 중요한 포인트를 설명하겠습니다:

# git push: 변경 사항을 원격 저장소로 푸시하는 명령입니다.
# --set-upstream 또는 -u 옵션: 원격 브랜치와 로컬 브랜치를 연결하고 향후에 git push 및 git pull 명령을 사용할 때 자동으로 연결된 원격 브랜치를 참조하도록 설정합니다.
# origin: 기본 원격 저장소의 별칭입니다. 보통 이 별칭은 원격 저장소의 URL을 나타냅니다.
# master: 로컬 브랜치의 이름입니다. 여기서는 로컬 브랜치를 원격 저장소의 마스터 브랜치로 푸시하려고 합니다.
# --set-upstream 옵션을 사용하면 원격 저장소의 브랜치와 로컬 브랜치를 연결하고, 이후에 git push 명령을 사용할 때 브랜치를 명시적으로 지정하지 않아도 됩니다. 예를 들어, 다음과 같이 간단하게 git push를 사용할 수 있습니다: